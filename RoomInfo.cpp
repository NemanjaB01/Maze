///---------------------------------------------------------------------------------------------------------------------
std::string RoomInfo::getRoomInfoString(char room_id)
{
    if (room_id < 'A' || room_id > 'Z')
    {
        return "";
    }
    
    room_id -= 'A';
    return room_info_array.at(room_id);
}

